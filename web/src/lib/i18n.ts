export function getTranslations(locale: Locale): Record<string, string> {
  return translations[locale] || translations.en;
}

export function t(locale: Locale, key: string, params?: Record<string, string>): string {
  const dict = translations[locale] || translations.en;
  let value = dict[key] || translations.en[key] || key;
  if (params) {
    for (const [k, v] of Object.entries(params)) {
      value = value.replace(`{${k}}`, v);
    }
  }
  return value;
}

const LOCALE_COOKIE = "cdc_locale";

export function getLocaleFromCookie(cookieHeader?: string): Locale {
  if (!cookieHeader) return "en";
  const match = cookieHeader.match(new RegExp(`${LOCALE_COOKIE}=([^;]+)`));
  const val = match?.[1] as Locale | undefined;
  if (val && SUPPORTED_LOCALES.includes(val)) return val;
  return "en";
}

